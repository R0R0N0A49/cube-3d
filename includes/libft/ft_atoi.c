#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int		sign;
	char	*str;
	int		res;
	int		i;

	res = 0;
	i = 0;
	sign = 1;
	str = (char *) nptr;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}
	return (res * sign);
}
